#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		if (n <= -10)
			ft_putnbr(n / 10);
		else
			ft_putchar('-');
		ft_putchar((n % 10) * -1 + 48);
	}
	else
	{
		if (n >= 10)
			ft_putnbr(n / 10);
		ft_putchar((n % 10) + 48);
	}
}

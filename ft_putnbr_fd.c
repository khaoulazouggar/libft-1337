#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		if (n <= -10)
			ft_putnbr_fd(n / 10, fd);
		else
			ft_putchar_fd('-', fd);
		ft_putchar_fd((n % 10) * -1 + 48, fd);
	}
	else
	{
		if (n >= 10)
			ft_putnbr_fd(n / 10, fd);
		ft_putchar_fd((n % 10) + 48, fd);
	}
}

#include "libft.h"

int		countwords(char *s, char c)
{
	int i;
	int wrd;

	wrd = 0;
	i = 0;
	while (*s)
	{
		if (*s == c)
			i = 0;
		else if (++i == 1)
			wrd++;
		s++;
	}
	return (wrd);
}
